package sofi;

public class MathExample {

	public static void main(String[] args) {
		double x;
		x = Math.max(22, 44);
		System.out.println("The maximum among 22 and 44 :: " + x);
		
		x = Math.sqrt(8);
		System.out.println("The square root of 8 is :: " + x);
		
		x = Math.abs(-55);
		System.out.println(" The absolute value of -55 is :: " + x);
		
		x = Math.pow(2, 4);
		System.out.println(" The cube of 2 is equal to :: " + x);
		
		x = Math.min(22, 33);
		System.out.println("The minimum among 22 and 33 is :: " + x);
       
		x = Math.log(3);
		System.out.println("The natural log of 3 is :: " + x);
	}

}
